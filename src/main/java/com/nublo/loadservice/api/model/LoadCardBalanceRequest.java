package com.nublo.loadservice.api.model;

/**
 * @author DELL
 *
 */
public class LoadCardBalanceRequest {
	private String mobileNumber;
	private String emailId;
	private String cardNumber;
	private String expiryDate;
	private String errorCode;
	private String errDescription;
	private String bankId;
	private Double loadAmount;
	private Double balance;

	/**
	 * @return the mobileNumber
	 */
	public String getMobileNumber() {
		return mobileNumber;
	}

	/**
	 * @param mobileNumber the mobileNumber to set
	 */
	public void setMobileNumber(String mobileNumber) {
		this.mobileNumber = mobileNumber;
	}

	/**
	 * @return the emailId
	 */
	public String getEmailId() {
		return emailId;
	}

	/**
	 * @param emailId the emailId to set
	 */
	public void setEmailId(String emailId) {
		this.emailId = emailId;
	}

	/**
	 * @return the cardNumber
	 */
	public String getCardNumber() {
		return cardNumber;
	}

	/**
	 * @param cardNumber the cardNumber to set
	 */
	public void setCardNumber(String cardNumber) {
		this.cardNumber = cardNumber;
	}

	/**
	 * @return the expiryDate
	 */
	public String getExpiryDate() {
		return expiryDate;
	}

	/**
	 * @param expiryDate the expiryDate to set
	 */
	public void setExpiryDate(String expiryDate) {
		this.expiryDate = expiryDate;
	}

	/**
	 * @return the errorCode
	 */
	public String getErrorCode() {
		return errorCode;
	}

	/**
	 * @param errorCode the errorCode to set
	 */
	public void setErrorCode(String errorCode) {
		this.errorCode = errorCode;
	}

	/**
	 * @return the errDescription
	 */
	public String getErrDescription() {
		return errDescription;
	}

	/**
	 * @param errDescription the errDescription to set
	 */
	public void setErrDescription(String errDescription) {
		this.errDescription = errDescription;
	}

	/**
	 * @return the bankId
	 */
	public String getBankId() {
		return bankId;
	}

	/**
	 * @param bankId the bankId to set
	 */
	public void setBankId(String bankId) {
		this.bankId = bankId;
	}

	/**
	 * @return the balance
	 */
	public Double getBalance() {
		return balance;
	}

	/**
	 * @param balance the balance to set
	 */
	public void setBalance(Double balance) {
		this.balance = balance;
	}

	/**
	 * @return the loadAmount
	 */
	public Double getLoadAmount() {
		return loadAmount;
	}

	/**
	 * @param loadAmount the loadAmount to set
	 */
	public void setLoadAmount(Double loadAmount) {
		this.loadAmount = loadAmount;
	}

}
